"""
Two-fer or 2-fer is short for two for one. One for you and one for me.
"""


def two_fer(name="you"):
    """
    Given a name, return a string "you"
    Where "name" is the given it will show the name.

    :return: string - "One for 'name', one for me." or "One for you, one for me."
    """
    return f"One for {name}, one for me."
